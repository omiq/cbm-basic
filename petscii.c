const char *petscii_char_to_utf8(uint8_t c) {
    const char *s = petscii_safe_to_utf8[c];
    return s ? s : "?";
}

void print_petscii(const uint8_t *data, size_t len) {
    for (size_t i = 0; i < len; i++) {
        fputs(petscii_char_to_utf8(data[i]), stdout);
    }
}

